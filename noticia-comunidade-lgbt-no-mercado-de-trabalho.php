                        <p id="artigop">Se tratando de transexuais, o assunto fica ainda mais delicado. Segundo dados da Associação Nacional de Travestis e Transexuais (Antra) levantado em 2020, apenas 4% das pessoas transexuais possuem emprego formal, 6% possuem emprego informal e cerca de 90% trabalham com prostituição.  </p>
                        <p id="artigop">“A prostituição é uma realidade na vida de mulheres trans, infelizmente, já que muitas de nós são expulsas de casa ainda adolescente, como aconteceu comigo. Sem estudo e oportunidade, essa foi a única chance que me restou. E não é coisa da nossa cabeça, é real”, diz influenciadora e atriz, Ariadna Arantes. Ainda adolescente Ariadna teve seu primeiro trabalho como office “boy", já nessa época ela possuía um jeito afeminado e se sentia na obrigação de “disfarçar”, além de “engrossar a voz” por medo de ser excluída e perder o emprego. </p>
                        <p id="artigop">A atual assessora de financiamento da Prefeitura de Francisco Morato, Larissa Raniel, também conta um pouco de sua dificuldade, de não ter sido aceita em trabalhos formais por estar fora dos padrões - “Quando uma mulher trans e travesti se descobre, a prostituição é a profissão que nos acolhe. Nossa primeira porta é a esquina”. “A nossa escola é a esquina pela falta de oportunidade. Hoje, muitas de nós pensam mais em estudar e correm atrás de capacitação, mas do que adianta ser capacitada e não ter uma chance de exercer? Esse é um medo nosso”. </p>
                        
