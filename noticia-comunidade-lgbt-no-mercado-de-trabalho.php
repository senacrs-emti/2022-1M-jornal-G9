                        <p id="artigop">Em 28 de julho de 1969 ocorreu em Nova York a primeira manifestação LGBT do mundo, após esse dia, foram criadas diversas organizações em prol dos direitos igualitários para a comunidade LGBT. Entretanto, mesmo depois de muitas lutas e ganhos de direitos, ainda existem diversas dificuldades para aqueles da comunidade, tanto em questões da vida em si, quanto no mercado de trabalho.  </p>
                        <p id="artigop">Apenas em 1990 a Organização Mundial da Saúde retirou a homossexualidade da lista de distúrbios psiquiátricos de sua Classificação Estatística Internacional de Doenças (CID), porém apenas em 2018 a OMS deixou de considerar a transexualidade como uma doença. Mesmo que leis contra a LGBTfobia tenham sidos criadas (ainda que depois dos anos 2000), existem mais de 70 países que consideram LGBT um crime – em alguns julgados como pena de morte, e mesmo “protegidos”, o Brasil ainda é o país onde mais matam LGBT no mundo. </p>
                        <p id="artigop">Conseguir um trabalho formal e digno em um país como o Brasil não é fácil, principalmente para aqueles da comunidade LGBT. De acordo com uma pesquisa realizada pela Center for Talent Innovation, 33% das empresas no Brasil não contratariam pessoas LGBTQIA+ para cargos de chefia, e 41% dos funcionários LGBTQIA+ afirmam já terem sofrido algum tipo de discriminação em razão de sua orientação sexual ou identidade de gênero no ambiente de trabalho. Em outra pesquisa, desta vez realizada pela empresa de consultoria Santo Caos, foi constatado que 61% dos funcionários LGBT no Brasil optam por esconder de colegas e gestores a sua orientação sexual por medo de exclusão social e possíveis demissões. Outro dado relacionado a comunidade geral LGBT que é importante ser levantado, é uma pesquisa levantada pelo LinkedIn: 35% das pessoas que foram entrevistadas já sofreram algum tipo de discriminação em alguma empresa, a pesquisa também revelou que piadas e comentários homofóbicos foram os mais citados entre as formas de discriminação atual. </p>
                        <p id="artigop">A partir dos dados apresentados, é possível perceber que a principal dificuldade dos LGBT no mercado de trabalho é a homofobia. Mesmo com a existência da Lei 7.716/1989, não existe nenhum órgão de monitoramento para garantir que essa lei seja cumprida, e dificilmente alguém que pratica atos de homofobia ou assédio é punido corretamente, seja pela justiça falha do Brasil, ou pela falta de coragem da vítima de denunciar o ocorrido. </p>
                        
                        <center>
