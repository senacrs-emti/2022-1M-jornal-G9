                        <p id="artigop">Os relatos acima, são de duas mulheres que tiveram que se esforçar o máximo possível para conseguir viver com dignidade depois de sofrer muito na mão do mundo. São um símbolo de resistência, mas a luta ainda está longe de acabar. Depois da primeira manifestação em 1969 houve muitas mudanças, muitas vitórias, entretanto, é de conhecimento geral que essa luta não ainda não chegou nem na metade de seu caminho. </p>
                        <p id="artigop">Entre 2019 até os dias de hoje, a inclusão LGBT é uma pauta deveras importante em grandes e pequenas empresas, principalmente pelo fato que, estimasse que companhias com times executivos com diversidade de gênero sejam até 33% mais lucrativas. Porém ainda assim, garantir oportunidades igualitárias de desenvolvimento e crescimento de carreira, é um dos maiores desafios para a comunidade. </p>
                        <p id="artigop">Mesmo que em passos curtos, a comunidade LGBT vem ganhando cada vez mais, espaços “seguros”. Como a Disney, que está apostando muito no público LGBT recentemente, e, mesmo que só visando lucro, ainda é um espaço a mais conquistado pela comunidade. E isso mesmo que pouco, reflete a consciência e empatia das empresas com um dos temas mais relevantes do mundo atualmente. O incentivo ao público LGBT no mercado de trabalho também é um impulso à diversidade, que promove novas visões e referências dentro das empresas, criando assim, um ambiente muito mais favorável à inovação. </p>
                
