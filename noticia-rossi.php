                        <p id="artigop">Sandman é o responsável pelo Mundo dos Sonhos, tendo acesso aos sonhos de todas as criaturas que sejam capazes de sonhar. A história da série tem início quando ele é preso em 1916, após se envolver em uma briga entre um mago e a Morte. Morpheus só consegue fugir depois de 106 anos em cárcere. A série The Sandman foi lançada em agosto desse ano na Netflix, com a direção de Allan Heinberg.</p>
                        <p id="artigop">A representação é bem diversificada, tendo Alex Burgess, Johanna Constantine, Desire, e muito mais. A série foi baseada nas HQs de The Sandman, que também possuem muita representação LGBTQIAP+. </p>
