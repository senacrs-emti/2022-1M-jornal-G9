                        <p id="artigop">1. Vingadores – A Cruzada das Crianças.</p>
                        <p id="artigop">Os Jovens Vingadores retornam em uma saga épica assinada pelos criadores da série Allan Heinberg e Jim Cheung. Quando os poderes de alteração da realidade de Wiccano começam a rivalizar com os da Feiticeira Escarlate, o jovem herói parte em uma busca por todo o Universo Marvel para encontrá-la. </p>
                        <center>
