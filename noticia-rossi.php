                        <p id="artigop">Como comunidade LGBTQIAP+ é representada na área do entretenimento? Ou seja, filmes, séries, literatura, etc. Esta comunidade aos poucos está sendo mais apresentada nas mídias, mas ainda há um longo caminho a ser percorrido. </p>
                        <p id="artigop">Apresento-lhes uma pequena lista de filme, séries e livros com a temática LGBTQIAP+: </p>
                        <p id="artigop"><b><h3>Filmes:</h3></b></p> 
                        <p id="artigop">1. Com amor, Simon:</p>
                        <p id="artigop">Este filme se trata de um adolescente gay que tem dificuldade em se assumir. Até que um dia começa a trocar cartas com outro garoto da escola em que estuda, se apaixonando por ele. O filme é uma produção estadunidense, dirigida por Greg Berlanti e foi lançado em 2018. Este filme teve uma grande audiência na época de seu lançamento.</p>
                        <center>
                            <img class="imgartigos" src="https://idris.com.br/dev/wp-content/uploads/2018/05/com-amor-simon.png" alt="">
