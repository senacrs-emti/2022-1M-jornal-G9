                        </center>
                        <p id="artigop">2. The Sun and The Star:</p>
                        <p id="artigop">Este livro foi escrito por Rick Riordan, também autor de Percy Jackson e os Olimpianos, e Mark Oshiro. Ainda não foi lançado, mas está programado para ser levado ao público em maio de 2023.  Este livro se passa no universo de Percy Jackson, em que os deuses da mitologia grega existem e coexistem com os seres humanos.  </p>
